    <h2>Bubur Manado</h2>

    <p>Tinutuan atau Bubur Manado adalah makanan khas Indonesia dari Manado, Sulawesi Utara. Ada juga yang mengatakan tinutuan adalah makanan khas Minahasa, Sulawesi Utara. Tinutuan merupakan campuran berbagai macam sayuran, tidak mengandung daging, sehingga makanan ini bisa menjadi makanan pergaulan antar kelompok masyarakat di Manado. Tinutuan biasanya disajikan untuk sarapan pagi beserta berbagai pelengkap hidangannya.</p>

    <h3>Bahan-bahan:</h3>
      <li>beras - 200 gram</li>
      <li>jagung manis dipipil - 2 buah</li>
      <li>singkong potong dadu - 150 gram</li>
      <li>ubi kuning, potong dadu - 100 gram</li>
      <li>labu kuning, kukus dan lumatkan dengan garpu - 1 kg</li>
      <li>air atau tambahkan sesuai kebutuhan - 4 liter</li>

    <h3>Bahan Halus:</h3>
      <li>5 lembar daun gedi,rajang halus</li>
      <li>1 lembar daun kunyit,rajang sehalus mungkin</li>
      <li>2 batang daun bawang, rajang halus</li>
      <li>1 ikat kangkung, ambil daun dan pucuk mudanya, rajang kasar</li>
      <li>1 ikat kecil bayam, ambil daun</li>
    <li>1 mangkuk kemangi, dari 5 ikat kecil kemangi, ambil daun dan pucuk mudanya saja</li>

    <h3>Bumbu:</h3>
      <li>5 batang serai, ambil bagian putihnya saja dan memarkan</li>
      <li>2 lembar daun salam</li>
      <li>1 sdm garam</li>
      <li>kaldu jamur secukupnya</li>
      <li>6 bawang putih, cincang halus</li>
      <li>6 bawang merah iris tipis</li>

    <h3>Cara Membuat:</h3>
      <li>Masukkan bawang putih, bawang merah, daun salam dan serai sambil terus diaduk, masukkan jagung dan labu kuning</li>
      <li>Tambahkan air bila diperlukan, masukkan garam lada dan kaldu jamur aduk rata lagi</li>
      <li>Setelah beras sudah menjadi bubur terakhir masukan semua sayuran aduk rata, koreksi rasa, angkat</li>

    <h3>Tips:</h3>
      <li>Bisa menambahkan sambal dabu dabu atau sambal korek.</li>
      <li>Memakai ikan asin goreng, lebih nikmat.</li>

