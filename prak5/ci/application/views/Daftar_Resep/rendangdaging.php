    <h2>Rendang Daging</h2>

<p>Rendang adalah masakan daging yang berasal dari Minangkabau. Masakan ini dihasilkan dari proses memasak suhu rendah dalam waktu lama menggunakan aneka rempah-rempah dan santan. Proses memasaknya memakan waktu berjam-jam (biasanya sekitar empat jam) hingga yang tinggal hanyalah potongan daging berwarna hitam pekat dan dedak. Dalam suhu ruangan, rendang dapat bertahan hingga berminggu-minggu. Rendang yang dimasak dalam waktu yang lebih singkat dan santannya belum mengering disebut kalio, berwarna cokelat terang keemasan.</p>

    <h3>Bahan-bahan:</h3>
<li>500 gram daging sapi, potong kotak jadi 8 atau 10 bagian</li>
<li>600 ml santan kental, dari satu setengah butir kelapa</li>
<li>5 sdm minyak goreng</li>
<li>300 ml air</li>

    <h3>Bahan Bumbu Halus:</h3>
<li>10 buah cabai merah iris</li>
<li>6 butir bawah merah</li>
<li>3 siung bawah putih</li>
<li>3 cm kunyit</li>
<li>3 cm jahe</li>
<li>3 cm lengkuas</li>
<li>1 sdm garam</li>
<li>2 lembar daun kunyit</li>
<li>1 batang serai</li>
<li>2 butir asam kandis</li>

    <h3>Cara Membuat:</h3>
<li>Tumis bumbu hingga harum, masukkan daging, aduk hingga berubah warna.</li>
<li>Masukkan daun kunyit, serai, dan asam kandis. Aduk perlahan.</li>
<li>Masukkan air. Masak hingga air menyusut. Aduk perlahan sesekali.</li>
<li>Masukkan santan, masak dengan api sedang.</li>
<li>Biarkan hingga daging empuk, kuah kering, dan berminyak. Angkat, sajikan.</li>

