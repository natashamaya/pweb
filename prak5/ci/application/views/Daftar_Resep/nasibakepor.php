    <h2>Nasi Bakepor</h2>

  <p>Nasi ini semacam nasi liwet yang disajikan dengan lauk pauk, sambal, sayuran, dan nasi putih. Nasi ini termasuk populer dan dijual di warung-warung nasi di sana. Konon, nasi bakepor merupakan warisan dari Kerajaan Kutai Kartanegara. Nasi tersebut hanya dihidangkan untuk anggota keluarga kerajaan saja. Saat itu, makanan berbahan beras ini tak bisa dinikmati oleh masyarakat biasa.</p>

    <h3>Bahan-bahan:</h3>
<li>300 gr beras putih, cuci bersih</li>
<li>2 lembar daun salam</li>
<li>1 lembar daun pandan, potongÂ²</li>
<li>4 sdm minyak sayur</li>
<li>1 sdt garam</li>
<li>400 ml air</li>
<li>75 gr ikan asin gabus, goreng, lalu suwir - suwir</li>
<li>15 lembar daun kemangi</li>
<li>3 buah cabe rawit, iris bulat tipis</li>
<li>1/2 buah jeruk nipis, ambil airnya</li>

    <h3>Bahan Sambel Raja:</h3>
<li>1 buah terong, potong dadu 1 cm</li>
<li>10 lonjor kacang panjang, potong 2 cm</li>
<li>10 siung bawang merah, belah 4</li>

    <h3>Bumbu Halus:</h3>
<li>10 buah cabe rawit</li>
<li>10 buah cabe merah besar, buang biji</li>
<li>2 buah jeruk limau</li>
<li>1 bks kecil terasi</li>
<li>1 sdt garam</li>
<li>1 sdm gula pasir</li>


    <h3>Cara Membuat:</h3>
<li>Goreng ikan asin gabus. Angkat. Tiriskan. Suwir - suwir. Sisihkan</li>
<li>Nasi Bakepor : Siapkan panci tebal. Masukkan ke dalamnya beras, air, minyak, daun salam, daun pandan, dan garam.</li>
<li>Masak di atas api kecil sambil diaduk. Angkat.</li>
<li>Masukkan daun kemangi, ikan asin suwir, cabai, dan air jeruk nipis, aduk rata.</li>
<li>Tutup panci kemudian Masak dengan api sangat kecil hingga tanak dan nasi matang.</li>
<li>Buka tutup panci biarkan hingga permukaan nasi tampak kering dan bagian pinggirnya berkerak. Matikan api.</li>
<li>Sambal Raja : Goreng kacang panjang, terong dan bawang merah sebentar dengan minyak panas.Tiriskan.</li>
<li>Ulek cabe rawit, cabe merah, terasi, garam dan gula pasir. Beri perasan jeruk limau. (Kulit jeruk jangan di buang, iris halus kulit jeruk).</li>
<li>Tumis sambal hingga harum, masukkan kacang panjang, terong dan bawang merah yang telah di goreng tadi.</li>
<li>Aduk rata sebelum di angkat masukkan irisan kulit jeruk limau. Aduk rata. Matikan api.</li>
<li>Sambal Raja siap di dampingkan dengan nasi bakepor.</li>

