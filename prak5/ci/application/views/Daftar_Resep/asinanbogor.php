    <h2>Asinan Bogor</h2>

<p>Asinan Bogor, makanan yang tebuat dari aneka sayur dan buah segar yang diambah tempe dan tahu gireng, kerupuk, dan kacang goreng ini tak pernah sepi penggemar. Asinan tersebut dibuat dengan cara pengasinan buah dan sayur dengan garam atau pengasaman menggunakan cuka dan cabai rawit. Mengacu dari cara pembuatannya, asinan Bogor menggunakan proses pengawetan sayur dan buah menggunakan cara alami.</p>

    <h3>Bahan-bahan Asinan:</h3>
<li>buah kedondong secukupnya, dipotong-potong memanjang</li>
<li>salak, buang bijinya, dipotong-potong</li>
<li>1 buah nanas ukuran kecil, atau Â½ buah ukuran sedang, potong-potong</li>
<li>1 buah bengkuang secukupnya , diiris-iris</li>
<li>1 buah mangga mengkal, dipotong-potong, mangga Indramayu lebih cocok.</li>
<li>pepaya mengkal secukupnya, dipotong-potong</li>
<li>jambu air merah secukupnya, dipotong-potong</li>
<li>1 buah ubi kuning, potong tipis</li>
<li>buah lainnya sesuai selera</li>


    <h3>Bahan kuah asinan:</h3>
<li>1 liter air</li>
<li>3 buah cabai merah</li>
<li>5 buah cabai merah besar, dibuang biji</li>
<li>2 buah cabai rawit merah</li>
<li>1 sendok teh ebi</li>
<li>5 sendok makan gula pasir</li>
<li>1 sendok makan garam</li>
<li>1 sendok makan cuka masak</li>

    <h3>Cara Membuat:</h3>
<li>Haluskan cabai merah, cabai besar, cabai rawit, garam, gula, ebi. Jangan terlalu halus, karena ulekan yang kasar adalah salah satu ciri khas asinan bogor.</li>
<li>Panaskan air, masukkan bumbu halus tersebut di atas, masak hingga mendidih, tambahkan cuka. Matikan api.</li>
<li>Masukkan buah-buahan ke dalam air bumbu, diamkan sampai uap panas hilang.</li>
<li>Masukkan lemari es kurang lebih 2-3 jam, sampai bumbu meresap ke buah-buahan.</li>
<li>Setelah kira-kira 2-3 jam dan rasa telah meresap maka asinan buah khas Bogor bisa Anda sajikan.</li>

