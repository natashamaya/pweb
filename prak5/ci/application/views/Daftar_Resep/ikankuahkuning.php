    <h2>Ikan Kuah Kuning</h2>

<p>Kepulauan Maluku yang kaya dengan hasil ikan laut dan rempah-rempah ini memang terkenal dengan masakan khas yang berasal dari ikan, salah satunya yaitu "Ikan Kuah Kuning". Sesuai namanya sajian ini berwarna kuning cenderung oranye yang berasal dari rempah kunyit. Sajian Ikan yang dimasak dengan daun kemangi, bumbu, rempah serta perasan jeruk nipis.</p>

    <h3>Bahan-bahan:</h3>
<li>500 gr ikan, potong-potong</li>
<li>2 buah jeruk nipis</li>
<li>2 lembar daun salam</li>
<li>2 cm lengkuas, geprek</li>
<li>Secukupnya cabe rawit, biarkan utuh</li>
<li>2 batang sereh, geprek</li>
<li>2 buah tomat, potong2</li>
<li>2 batang kemangi, siangi</li>
<li>2 batang daun bawang, iris</li>
<li>500 ml air</li>
<li>Secukupnya garam, gula, penyedap (optional)</li>

    <h3>Bumbu halus:</h3>
<li>8 butir bawang merah</li>
<li>5 butir bawang putih</li>
<li>3 cm kunyit</li>
<li>3 cm jahe</li>
<li>3 butir kemiri, sangrai</li>
<li>3 buah cabe merah besar (optional)</li>

    <h3>Cara Membuat:</h3>
<li>Ikan dicuci bersih, kucuri air jeruk nipis. Campur garam sedikit. Aduk rata. Diamkan 15 menit. Cuci bersih kembali.</li>
<li>Tumis bumbu halus bersama sereh, lengkuas, dan daun salam, sampai harum.</li>
<li>Masukkan ikan dan daun bawang, aduk rata.</li>
<li>Tuangkan air. Tunggu sampai air mendidih. Masukkan garam, gula, dan penyedap. Aduk rata. Masak sampai ikan matang sempurna.</li>
<li>Tambahkan perasan air jeruk nipis, cabe rawit, tomat, dan daun kemangi. Masak sebentar. Angkat.</li>

