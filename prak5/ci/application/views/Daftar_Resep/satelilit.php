    <h2>Sate Lilit</h2>

    <p>Sate lilit merupakan makanan khas Bali yang diolah menggunakan bahan daging, daging yang sering digunakan adalah daging babi, ikan, ayam, sapi dan kura-kura. Daging tersebut akan dihaluskan dan dicampur dengan parutan kelapa, santan, jeruk nipis, bawang merah dan merica. Daging yang telah dicampur itu akan dililitkan di bambu, batang serai atau tebu dan terakhir akan dipanggang. Kebanyakan sate memang ditusuk dengan tusuk sate yang tajam, untuk sate lilit ini membutuhkan tusuk sate yang permukaannya luas sehingga daging akan mudah untuk melekat ditusuk satenya.</p>

    <h3>Bahan-bahan:</h3>
<li>300 gr daging ayam (giling).</li>
<li>100 gr kelapa parut.</li>
<li>1 butir telur ayam.</li>
<li>2 sdm tapioka.</li>
<li>5 siung bawang merah.</li>
<li>4 siung bawang putih.</li>
<li>3 biji kemiri (sangrai).</li>
<li>1 cm kunyit (bakar).</li>
<li>2 cm kencur.</li>
<li>1 cm lengkuas.</li>
<li>1 cm jahe.</li>
<li>5 buah cabe rawit (boleh lebih).</li>
<li>1 cm terasi (bakar).</li>
<li>secukupnya garam & merica.</li>
<li>20 batang bambu atau serai.</li>

    <h3>Cara Membuat:</h3>
<li>Haluskan bawang merah,bawang putih, kunyit, cabai, kemiri, jahe, kencur, lengkuas dan terasi.</li>
<li>Campurkan ayam giling,telur,tapioka,dan kelapa. Beri garam dan merica secukupnya.</li>
<li>Tumis bumbu halus hingga harum lalu campurkan kedalam adonan daging ayam. Aduk rata.</li>
<li>Panaskan Grill Pan dengan api sedang. Ambil sedikit adonan. Masak hingga matang. Lalu cicipi rasanya hingga pas.</li>
<li>Jika rasa sudah pas. Ambil adonan secukupnya dan lilitkan pada batang Bambu atau Batang serai. Lalu panggang di Grill Pan hingga matang.</li>
<li>Untuk Sambal matah : iris bawang merah,daun jeruk,cabai,dan batang serai bagian putihnya saja.</li>
<li>Beri sejumput garam. panaskan 2 sdm minyak masukkan terasi. goreng hingga terasi hancur bercampur minyak. lalu campurkan pada bahan irisan. Aduk rata, siap dihidangkan.</li>

