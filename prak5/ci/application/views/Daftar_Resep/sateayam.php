    <h2>Sate Ayam Madura</h2>

  <p>Sate ayam madura adalah sate yang meiliki bumbu khas Madura. Sate Madura biasanya terbuat dari ayam. Madura selain terkenal sebagai pulau garam, juga terkenal dengan satenya. Sate madura sudah terkenal di seluruh Nusantara, Sate Madura dapat ditemukan hampir di semua daerah khususnya di kota besar seperti Medan, Jakarta, Bandung, dan Surabaya. Konon di Madura sendiri sate susah dicari.</p>

    <h3>Bahan-bahan:</h3>
<li>250 g fillet ayam, dipotong dadu, kemudian tusukan ke tusukan sate</li>
<li>1 buah jeruk nipis yang telah iris</li>
<li>100 ml minyak goreng</li>
<li>3 butir bawang merah, diiris tipis</li>
<li>5 sdm kecap manis</li>
<li>nasi putih atau bisa diganti lontong secukupnya</li>


    <h3>Bahan Bumbu Kacang:</h3>
<li>3 butir bawang putih</li>
<li>4 butir bawang merah</li>
<li>100 g kacang tanah, digoreng</li>
<li>2 butir kemiri</li>
<li>garam secukupnya</li>
<li>150 ml air kaldu ayam</li>
<li>2 sdm minyak goreng</li>

    <h3>Bahan Sambel:</h3>
<li>cabe merah kecil 15 buah</li>
<li>bawang putih 4 siung</li>
<li>garam secukupnya</li>
<li>minyak goreng secukupnya</li>

    <h3>Cara Membuat:</h3>
<li>Pembuatan bumbu kacang,  haluskan bawang putih, bawang merah, kacang tanah, garam, dan juga kemiri.  Tumis bumbu kacang hingga tercium bau harum, kemudian masukkan air kaldu dan selanjutnya masak sampai mengental lalu angkat. Sisihkan.</li>
<li>Selanjutnya , campurkan 2 sendok makan bumbu kacang dengan ditambah 2 sendok makan kecap manis, lalu celupkan sate, kemudian bakar hingga setengah matang. Angkat lalu gulingkan lagi dalam bumbu, terus  bakar lagi hingga matang, angkat.</li>
<li>Untuk sambal bawang,  Tumis bawang putih sampai setengah matang lalu angkat. Selanjutnya campur bawang putih ditambah juga cabe merah kecil, dan garam, haluskan.</li>

