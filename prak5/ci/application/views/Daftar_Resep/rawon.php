    <h2>Rawon</h2>

  <p>Rawon adalah makanan tradisional dari Jawa Timur berupa sup daging dengan kuah yang berwarna hitam. Warna hitam dari kuah Rawon ini terbuat dari kluwak dan dimasak dengan bumbu khusus sehingga menghasilkan aroma dan rasa yang khas sehingga sangat menggugah selera. Rawon ini merupakan salah satu makanan tradisional yang terkenal dan menjadi salah satu icon kuliner di Jawa Timur.</p>

    <h3>Bahan Halus:</h3>
<li>6 siung bawang putih</li>
<li>4 biji keluak (ambil isinya, lalu rendam dalam air)</li>
<li>8 buah cabai merah</li>
<li>2 ibu jari lengkuas</li>
<li>3 cm kunyit</li>
<li>2 cm kencur</li>
<li>2 cm jahe</li>
<li>2 sdm ketumbar</li>
<li>1/4 sdt jintan</li>
<li>1/2 sdt merica butir</li>
<li>4 lembar daun salam</li>
<li>2 batang serai, memarkan</li>
<li>2 mata asam jawa, larutkan dengan 1 sdm air</li>
<li>5 lembar daun jeruk purut</li>

    <h3>Cara Membuat:</h3>
<li>Cuci daging sampai bersih, rebus sampai matang. Angkat dan tiriskan daging, lalu potong ukuran dadu. Sisihkan daging dan air kaldunya.</li>
<li>Panaskan minyak, tumis bumbu halus sampai harum dan matang. Masukkan daging, serai, daun jeruk, air asam, dan garam. Aduk sampai rata dan beraroma.</li>
<li>Pukul-pukul daging sampai melebar. Goreng dalam minyak panas sebentar, angkat. Taburi dengan bawang goreng.</li>

