    <h2>Batagor Bandung</h2>

    <p>Batagor adalah makanan khas Bandung. Batagor mirip dengan siomay yang digoreng. Batagor sendiri singkatan dari Bakso Tahu Goreng. Bahan utamanya mirip siomay, yakni adonan ikan tenggiri dan tepung tapioka. Lalu digoreng. Begitu juga dengan tahu-nya juga digoreng. Menu pendukung batagor biasanya bumbu kacang, kecap manis, sambal, dan air perasan jeruk nipis sebagai pelengkap. Batagor ini jajanan yang populer di Indonesia. Terkadang pedagang siomay juga berjualan batagor. Bila siomay dikukus maka batagor digoreng. Bila siomay bentuknya “gepeng” maka batagor bentuknya bulat seperti bakso. Batagor dapat ditemukan di kaki lima atau warung. Batagor ada beberapa variasi, antara lain batagor kuah dan batagor pangsit.</p>

    <h3>Bahan-bahan:</h3>
<li>250 gr ikan fillet tenggiri buang kulitnya</li>
<li>2 butir telur (jika besar boleh pakai 1 butir)</li>
<li>5 sdm tepung tapioka/sagu</li>
<li>2 sdm tepung terigu</li>
<li>4 bh bawang putih potong goreng</li>
<li>1/2 sdt meres garam</li>
<li>1 sdt gula putih</li>
<li>1/2 sdt lada putih bubuk (optional)</li>
<li>1 lembar daun bawang iris tipis</li>
<li>5 buah tahu pong segi4 bagi 2</li>
<li>15 lembar kulit pangsit</li>

    <h3>Cara Membuat:</h3>
<li>Masukan seluruh bahan kedalam chopper, blender hingga halus, koreksi rasa.</li>
<li>Campurkan irisan daun bawang, aduk hingga rata.</li>
<li>Isi tahu dengan 1 sdm adonan, isi kulit pangsit dengan 1sdt adonan, Goreng dalam api sedang hingga kecoklatan.</li>

    <h3>Bahan Bumbu Kacang:</h3>
<li>100 gr kacang tanah</li>
<li>6 bh cabai merah keriting</li>
<li>6 bh cabai rawit merah galak</li>
<li>3 bh bawang putih</li>
<li>100 cc air putih</li>
<li>1/2 sdt garam</li>
<li>1 sdm gula merah</li>

    <h3>Cara Membuat:</h3>
<li>Goreng seluruh bahan.</li>
<li>Blender dengan air, garam, gula.</li>
<li>Siapkan sedikit minyak, masak dalam api kecil hingga mengeluarkan minyak.</li>

